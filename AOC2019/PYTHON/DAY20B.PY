from collections import deque
import string

class DonutMaze:
    def __init__(self, maze_input):
        self.grid = [list(line) for line in maze_input.splitlines() if line]
        self.width = len(self.grid[0])
        self.height = len(self.grid)
        self.portals = self.find_portals()

    def find_portals(self):
        # Find all the portal locations and build a mapping from portal name to coordinates.
        portals = {}
        
        def add_portal(name, x, y):
            if name not in portals:
                portals[name] = []
            portals[name].append((x, y))

        for y in range(self.height - 1):
            for x in range(self.width - 1):
                # Identify the letters for the labels of portals
                if self.grid[y][x] in string.ascii_uppercase:
                    if x + 1 < self.width and self.grid[y][x + 1] in string.ascii_uppercase:
                        # Horizontal portal label
                        label = self.grid[y][x] + self.grid[y][x + 1]
                        if x > 0 and self.grid[y][x - 1] == '.':
                            add_portal(label, x - 1, y)
                        elif x + 2 < self.width and self.grid[y][x + 2] == '.':
                            add_portal(label, x + 2, y)
                    if y + 1 < self.height and self.grid[y + 1][x] in string.ascii_uppercase:
                        # Vertical portal label
                        label = self.grid[y][x] + self.grid[y + 1][x]
                        if y > 0 and self.grid[y - 1][x] == '.':
                            add_portal(label, x, y - 1)
                        elif y + 2 < self.height and self.grid[y + 2][x] == '.':
                            add_portal(label, x, y + 2)
        
        # Include portals with single entry (such as 'AA' and 'ZZ')
        portals = {name: locs for name, locs in portals.items() if len(locs) >= 1}
        return portals
    
    def bfs_recursive(self, start, end):
        queue = deque([(start, 0, 0)])  # (position, steps, level)
        visited = set([(start, 0)])
        
        while queue:
            (x, y), steps, level = queue.popleft()
            
            # Debugging output for level exploration
            if steps % 100 == 0:  # Reduce output frequency to improve readability
                print(f"Exploring position ({x}, {y}) at level {level} with {steps} steps.")
            
            # Check if we've reached the destination at the outermost level
            if (x, y) == end and level == 0:
                return steps
            
            # Add neighboring positions
            for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                nx, ny = x + dx, y + dy
                if 0 <= nx < self.width and 0 <= ny < self.height:
                    if (nx, ny, level) not in visited and self.grid[ny][nx] == '.':
                        visited.add((nx, ny, level))
                        queue.append(((nx, ny), steps + 1, level))
            
            # Check for portals
            if (x, y) in [loc for locs in self.portals.values() for loc in locs]:
                for name, locs in self.portals.items():
                    if (x, y) in locs:
                        # Determine if we are moving inward or outward
                        if len(locs) == 2:
                            other_side = locs[1] if locs[0] == (x, y) else locs[0]
                            if name not in ['AA', 'ZZ']:
                                if (x, y) in self.outer_portals():
                                    # Move outward (decrease level)
                                    new_level = level - 1
                                    if new_level >= 0:  # Only move outward if not going below level 0
                                        if (other_side, new_level) not in visited:
                                            visited.add((other_side, new_level))
                                            queue.append((other_side, steps + 1, new_level))
                                else:
                                    # Move inward (increase level)
                                    new_level = level + 1
                                    if (other_side, new_level) not in visited:
                                        visited.add((other_side, new_level))
                                        queue.append((other_side, steps + 1, new_level))
                            elif level == 0 and name == 'ZZ':
                                # Only allow 'ZZ' at the outermost level
                                if (other_side, level) not in visited:
                                    visited.add((other_side, level))
                                    queue.append((other_side, steps + 1, level))
                            elif name == 'AA' and level == 0:
                                # Start portal AA only usable at level 0
                                continue
        
        return -1  # If no solution found

    def outer_portals(self):
        # Determine which portals are on the outer edge of the maze
        outer_portals = set()
        for name, locs in self.portals.items():
            for x, y in locs:
                if x == 2 or x == self.width - 3 or y == 2 or y == self.height - 3:
                    outer_portals.add((x, y))
        return outer_portals

    def solve_recursive(self):
        if 'AA' not in self.portals or 'ZZ' not in self.portals:
            raise ValueError("Maze does not contain valid entry (AA) or exit (ZZ) portals.")
        start = self.portals['AA'][0]
        end = self.portals['ZZ'][0]
        return self.bfs_recursive(start, end)


