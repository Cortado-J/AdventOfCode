  struct Position: Hashable {
    var row: Int
    var col: Int
    
    var neighbours: [Position] {
      [Position(row: row-1, col: col-1),
       Position(row: row-1, col: col  ),
       Position(row: row-1, col: col+1),
       
       Position(row: row  , col: col-1),
       Position(row: row  , col: col+1),
       
