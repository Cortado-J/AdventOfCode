  func load() {
    grid = [:]
    for (row, line) in input.enumerated() {
      for (col, char) in line.enumerated() {
        grid[Position(row: row, col: col)] = char == "#" ? 1 : 0
      }
    }
  }
  
  func generate() {
    var work: [Position: Int] = [:]
    for (position, state) in grid {
      let neighbourCount = position.neighbours.map{ grid[$0] ?? 0 }.reduce(0,+)
      work[position] =
      (
        (state == 1 && (neighbourCount == 2 || neighbourCount == 3)) ||
        (state == 0 &&                         neighbourCount == 3 )
      ) ? 1 : 0
    }
    grid = work
  }
  
  load()
